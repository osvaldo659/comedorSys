                    jTextField10.getText() + "' WHERE semana = '" +jTextField11.getText() + "' and dia = 'VIERNES'";
            
            stat.executeUpdate(lunes);
            stat.executeUpdate(martes);
            stat.executeUpdate(miercoles);
            stat.executeUpdate(jueves);
            stat.executeUpdate(viernes);
            
